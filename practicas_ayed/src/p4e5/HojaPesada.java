package p4e5;

public class HojaPesada {

	private int dato;
	private int peso;
		
	public HojaPesada(int dato, int peso){
		this.dato = dato;
		this.peso = peso;		
	}
	
	public int getDato() {
		return dato;
	}
	
	public void setDato(int dato) {
		this.dato = dato;
	}
	
	public int getPeso() {
		return peso;
	}
	
	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	
}
